package com.lambton;

public class Arithmetic {

    int add(int a,int b)
    {
        int c ;
        c= a+b;

        return c;
    }

    int substract(int a, int b)
    {
        int c;

        c=a-b;
        return c;
    }

    int multiplication(int a, int b)
    {
        int c;

        c=a*b;
        return c;

    }

    int division(int a, int b)
    {
        int c;

        c=a/b;
        return c;
    }

}
